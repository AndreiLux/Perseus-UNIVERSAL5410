int board_universal_5410_init_gpio(struct sec_gpio_table_info *sec_gpio_table_info)
{
	if (IS_ERR_OR_NULL(sec_gpio_table_info)) {
		printk(KERN_INFO "%s: Invalid parameter\n", __func__);
		return -EINVAL;
	}

	sec_gpio_table_info->init_table = vienna_init_table;
	sec_gpio_table_info->nr_init_table = ARRAY_SIZE(vienna_init_table);
	sec_gpio_table_info->sleep_table = vienna_sleep_table;
	sec_gpio_table_info->nr_sleep_table = ARRAY_SIZE(vienna_sleep_table);

	return 0;
}
